# Estamos insertando un valor en la posición 2 de la lista

thislist = ["apple", "banana", "cherry"]
thislist.insert(2, "watermelon")
print(thislist)

# Con este metodo estamos agregando los elementos al final de la lista

thislist = ["apple", "banana", "cherry"]
thislist.append("orange")
print(thislist)

# Ahora, de esta forma estamos ingresando elementos en la posición que indiquemos en su indice
thislist = ["apple", "banana", "cherry"]
thislist.insert(1, "orange")
